package org.ServiceOffered;

public class ProductReview {
	private String ProductReviewId;
	private String ProductReviewName;
	private String ProductReviewSeller;
	private String ProductReviewIdDescription;
	private String ProductReviewIdScore;

	public String getProductReviewId() {
		return ProductReviewId;
	}

	public String getProductReviewName() {
		return ProductReviewName;
	}

	public String getProductReviewSeller() {
		return ProductReviewSeller;
	}

	public String getProductReviewIdDescription() {
		return ProductReviewIdDescription;
	}

	public String getProductReviewIdScore() {
		return ProductReviewIdScore;
	}

	public void setProductReviewId(String productReviewId) {
		ProductReviewId = productReviewId;
	}

	public void setProductReviewName(String productReviewName) {
		ProductReviewName = productReviewName;
	}

	public void setProductReviewSeller(String productReviewSeller) {
		ProductReviewSeller = productReviewSeller;
	}

	public void setProductReviewIdDescription(String productReviewIdDescription) {
		ProductReviewIdDescription = productReviewIdDescription;
	}

	public void setProductReviewIdScore(String productReviewIdScore) {
		ProductReviewIdScore = productReviewIdScore;
	}

}
